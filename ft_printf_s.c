#include "ft_printf.h"

int	ft_printf_s(char *str)
{
	int	i;

	i = 0;
	if (!str)
		return (write(1, "(null)", 6));
	while (str[i])
	{
		ft_printf_c(str[i]);
		i++;
	}
	return (i);
}
